package rs.ac.uns.ftn.asd.ProjekatSIIT2025.dto.rides;

public class RoutePointDTO {
    private double lat;
    private double lng;
    
    public RoutePointDTO() {
    }
    public double getLat() {
        return lat;
    }
    public void setLat(double lat) {
        this.lat = lat;
    }
    public double getLng() {
        return lng;
    }
    public void setLng(double lng) {
        this.lng = lng;
    }

    
}
